
public class AssignmentExample대입연산자 {
//AssignmentExample 할당,대입
	public static void main(String[] args) {
		// = 값을 대입하는 연산자
		// +=, -=, /=, *=, %= ..... 앞의 연산까지 같이 수행
		// ex : a += 3은  a = a+3과 같음
		int a = 1;
		a += 2; 
		System.out.println(a);
		
		int b = 1;
		b = b+2;
		System.out.println(b);

	}

}
